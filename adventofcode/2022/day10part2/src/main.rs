use std::io::{self, BufRead, Stdin};

struct InputUtils {
    stream: Stdin,
}

impl Default for InputUtils {
    fn default() -> Self {
        Self {
            stream: io::stdin(),
        }
    }
}

impl Iterator for InputUtils {
    type Item = String;

    fn next(&mut self) -> Option<Self::Item> {
        self.stream
            .lock()
            .lines()
            .next()
            .map(|line| line.unwrap().trim().to_string())
    }
}

fn process_tick(tick: &mut i64, x: &i64, screen: &mut String) {
    let draw_pos = *tick % 40;
    if (x - draw_pos).abs() <= 1 {
        screen.push('#');
    } else {
        screen.push('.');
    }
    *tick += 1;
    if *tick % 40 == 0 {
        screen.push('\n');
    }
}

fn solve(lines: Box<dyn Iterator<Item = String>>) -> String {
    let mut x: i64 = 1;
    let mut tick: i64 = 0;
    let mut screen: String = String::new();
    for line in lines {
        let parts: Vec<&str> = line.split_whitespace().collect();
        match parts[0] {
            "noop" => {
                process_tick(&mut tick, &x, &mut screen);
            }
            "addx" => {
                let value = parts
                    .last()
                    .and_then(|part| part.parse::<i64>().ok())
                    .expect("second argument to addx must be an integer");
                process_tick(&mut tick, &x, &mut screen);
                process_tick(&mut tick, &x, &mut screen);
                x += value;
            }
            _ => {
                unreachable!("Invalid instruction!")
            }
        }
    }
    screen
}

fn main() {
    let iu = InputUtils::default();
    let boxed_iter = Box::new(iu);
    println!("{}", solve(boxed_iter));
}

